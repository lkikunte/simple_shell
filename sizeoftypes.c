#include <stdio.h>
#include <stddef.h>

/**
*  main - prints the sizes of different types
*  Return - 0 if successfu
**/
int main()
{
	printf("Size of int is: %lu\n", sizeof(int));
	printf("Size of char is: %lu\n", sizeof(char));
	printf("Size of float is: %lu\n", sizeof(float));
	printf("Size of double is: %lu\n", sizeof(int));
	printf("Size of short int is: %lu\n", sizeof(int));
	printf("Size of unsigned int is: %lu\n", sizeof(int));
	printf("Size of long int is: %lu\n", sizeof(int));
	printf("Size of long long int is: %lu\n", sizeof(int));
	printf("Size of unsigned long int is: %lu\n", sizeof(int));
	printf("Size of unsigned long long int is: %lu\n", sizeof(int));
	return(0);
}
